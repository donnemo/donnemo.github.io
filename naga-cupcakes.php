			<p>Okay, you’re probably wondering what&nbsp;naga means. Several things
			actually. I can never just do food. There’s always some element of
			history, science, or perhaps even humor (as it should be). Today it’s
			history.</p>

			<p>Nāga is the Sanskrit and Pāli word for a deity or being, taking the
			form of a very great snake—specifically the King Cobra, found in
			Hinduism and Buddhism. The use of the term nāga is often ambiguous, as
			the word may also refer, in similar contexts, to one of several human
			tribes known as or nicknamed “Nāgas”; to elephants; and to ordinary
			snakes, particularly the King Cobra and the Indian Cobra, the latter of
			which is still called nāg in Hindi and other languages of India. So what
			 does this have to do with food? Some people think a certain chili
			pepper might be named after the Nāga .</p>

			<p>The Bhut Jolokia<strong>,</strong> as it is commonly known, is&nbsp;a chili pepper previously recognized by <em>Guinness World Records</em>
			 as the hottest pepper in the world. It is also called the&nbsp;Naga Jolokia
			and is believed to have originated from the hills of Nagaland. An
			alternative source for Naga jolokia&nbsp;claims that the name originates from
			 the ferocious Nāga warriors who once inhabited Nagaland. Spicy—warlike.
			 Sure, why not. This pepper’s scoville rating is insane.</p>

			<p>They include more than 20 tribes of mixed origin, varying cultures,
			and very different physiques and appearances. The numerous Nāga
			languages (sometimes classified as dialects) belong to the Tibeto-Burman
			 group of the Sino-Tibetan language family. Almost every village has its
			 own dialect; different groups of Nāgas communicate in broken Assamese
			(Nāgamese), or sometimes in English and Hindi.</p>

			<p>So, I named this cupcake after it’s Indo-Nagan, Sino-Tibetan heritage
			 of flavors. Don’t worry, the heat is what I’d consider mild and
			somewhat sneaky. It’ll warm up your mouth after you finish your first
			bite. On to the recipe.</p>

			<h2>Ingredients</h2>

			<h3><strong>For the cupcakes:</strong></h3>

			<ul><li>1/2 cup Guinness (seriously, it’s <em>required</em>. The rest is for the chef.)</li><li>1/2 cup coconut milk</li><li>1 cup (2 sticks) unsalted&nbsp;butter</li><li>3/4 cup unsweetened cocoa powder</li><li>2 cups flour</li><li>2 cups sugar</li><li>1 1/2 teaspoons baking soda</li><li>3/4 teaspoon salt</li><li>2 large eggs</li><li>2/3 cup sour cream</li><li>1 Tbsp vanilla extract</li><li>1 Tbsp curry powder (coriander, turmeric, cumin, fenugreek, cardamom,&nbsp;red pepper, etc.)</li><li>1/2 tsp nutmeg</li><li>1/2 tsp cloves</li><li>1/4 tsp ginger</li><li>1/2 tsp black pepper</li><li>1/2 tsp red pepper</li><li>1 tsp cayenne pepper</li></ul>

			<p>
			I strongly suggest playing with the spice
			measurements to suit your taste. The above amounts are approximations as
			 I typically start flinging spices like a mad scientist while baking.
			</p>

			<h3><strong>For the filling:</strong></h3>

			<ul><li>8 ounces bittersweet chocolate</li><li>1/3 cup coconut milk</li><li>1/3 cup Bailey’s Irish Cream (can substitute more coconut milk or another liquid)</li><li>2 tablespoons butter, room temperature</li><li>1 tsp cayenne pepper</li><li>1/2 tsp cinammon</li><li>1/2 tsp curry</li></ul>

			<h3><strong>For the frosting</strong></h3>

			<ul><li>4 cups confections’ sugar</li><li>1 lb cream cheese</li><li>1 stick unsalted butter, at room temperature</li><li>1/4 cup coconut milk</li><li>3 to 4 tablespoons Bailey’s Irish Cream (or more, to taste)</li><li>1 tsp vanilla extract</li><li>fresh shredded coconut (optional)</li></ul>

			<h2>Directions</h2>

			<p>Pre-heat oven to 350°F. Line 24 cupcake cups with liners.</p>

			<p>Bring 1/2 cup stout, 1/2 cup coconut milk,&nbsp;and 1 cup butter to simmer
			 in heavy large saucepan over medium heat. Add cocoa powder and whisk
			until mixture is smooth. Remove from heat.</p>

			<p>In a separate large bowl, whisk flour, sugar, baking soda, 3/4
			teaspoon salt, and rest of dry spices. Using an electric mixer, blend
			in&nbsp;eggs and sour cream. Then slowly add stout-chocolate mixture just
			until combined. Slowly mix or fold batter until completely combined.</p>

			<p>Taste time. Tweak spices as desired.</p>

			<p>Divide batter among cupcake liners, filling them 2/3 to 3/4 of the
			way. Bake 15-20 minutes&nbsp;until tester inserted into center comes out
			clean. Rotate them once front to back if your oven bakes unevenly.&nbsp;Cool
			cupcakes on a rack completely.</p>

			<p>While your cupcakes cool, go ahead and make the filling. Chop the
			chocolate into coarse but roughly even pieces, and transfer it to a
			heatproof bowl. Heat the coconut milk and bailey’s&nbsp;until it is simmering
			 (but not boiling) and pour it over the chocolate. Let it sit for about a
			 minute and then stir until smooth. (If this has not sufficiently melted
			 the chocolate, you can return it to a double-boiler or give it 20
			seconds in the microwave to help the chocolate get sufficiently melty).
			Add the butter and spices and stir until combined.</p>

			<p>Fill the cupcakes. Use a pastry filling injector or pastry bag with
			fine tip to squeeze ganache filling into center of cupcake. Just insert
			the tip about 1/3 of the way and squeeze. It’s okay to allow it overflow
			 across top of cupcake. I always do when filling. If you’re worried
			about drippage, stick the cupcakes into the&nbsp;refrigerator to&nbsp;allow the
			chocolate to set quickly.&nbsp;You can use a baster in a pinch if you have
			neither an injector nor pastry bag.</p>

			<p>Make the frosting. In an electric mixer, mix the butter and cream
			cheese on medium speed. You want to get it very light and fluffy. Slowly
			 add the confectioners’ sugar, starting with a few tablespoons at a time
			 of your sugar until the frosting looks thick enough to spread (for me,
			this was about 2 cups). At this point, add in the coconut milk,
			vanilla,&nbsp;and Irish Cream. Whip it until combined. Beat in as much or as
			little of the remaining confectioners’ sugar until the frosting has
			reached your desired consistency.</p>

			<p>Frost your cupcakes. If you want to get fancy, pipe the frosting on
			top in swirls or whirls—but if you just want to eat the cupcakes, frost
			using a spatula—and garnish with the shredded coconut.</p>
